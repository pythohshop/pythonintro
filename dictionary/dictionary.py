# Dictionary demonstration 

my_dict = {
    'a': 'appel',
    'b': 'ball',
    'c': 'cat',
    'd': 'dog',
    'e': 'etc'
}

# Adding a key value pair to a dict
my_dict['f'] = 'frog'

# Changing value of a key 
my_dict['b'] = 'no more ball. Is it now?'

# deleting a key value pair in a dict
del my_dict['a']
